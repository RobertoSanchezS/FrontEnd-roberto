// De igualdad se hace con 2 signos iguales " == " revisa si el valor es igual
console.log(5 == 12);
console.log(34 == 34);
console.log("hola" == "Hola");
console.log("saludos" == "saludos");

// De igualdad estricta se hacee con 3 signoas iguales " === " revisa si el valor es igual en numero y string 
console.log(12 === 12);
console.log(12 === "12");

// Mayor qué >
console.log(4 > 2);
console.log(4 > 32);
console.log(4 > 4);

// Mayor o igual qué >=
console.log(4 >= 2);
console.log(4 >= 4);

// Menor qué <
console.log(5 < 32);
console.log(15 < 2);

// Menor o igual qué <=
console.log(5 <= 32);
console.log(30 <= 3);

// Distinto qué simple !=
console.log("hola" != "Hola");
console.log("hola" != "hola");

// Distinto qué estricto !==
console.log(45 !== "45");
console.log(45 !== 45);

