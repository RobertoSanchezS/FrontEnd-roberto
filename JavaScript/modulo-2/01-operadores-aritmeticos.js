let numeroA = 14;
let numeroB = 32;

// Suma +
let resultadoSuma = numeroA + numeroB;
console.log(resultadoSuma);

// Resta -
let resultadoResta = numeroA - numeroB;
console.log(resultadoResta);

// Multiplicar *
let resultadoMultiplicacion = numeroA * numeroB;
console.log(resultadoMultiplicacion);

// Division / 
let resultadoDivision = 100 / 20;
console.log(resultadoDivision);

//Modulo % te da el residuo de una division
let resultadoModulo = 15 % 4;
console.log(resultadoModulo)

// Operacion Combinada - respetar el orden de la multiplicacion, division, suma y resta entre " () "
let operacionCombinada = 5 + ((10 * 4) / 2);
console.log(operacionCombinada);